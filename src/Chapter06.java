
public class Chapter06 {
	
	public static void main(String[] args) {
		long long_p = 10000000000L;
		System.out.println(long_p);
		
		double duble_p =  3.14d;
		System.out.println(duble_p);
		
		boolean boolean_p =  true;
		System.out.println(boolean_p);
		
		Integer integer_w = 100;
		System.out.println(integer_w);
		
		Float float_w = 1.4142F;
		System.out.println(float_w);
		
		Character character_w = 'う';
		System.out.println(character_w);
		
		int[] array = {0,10,20,30,40};
		System.out.println(array[3]);
	}

}
